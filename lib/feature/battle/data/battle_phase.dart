/// Фазы жизненного цикла Battle.
enum BattlePhase {
  /// Начальное состояние — ничего не происходит.
  idle,

  /// Подключение к WebSocket.
  connecting,

  /// Комната создана, ожидание участников.
  waitingRoom,

  /// Проверка комнаты по коду (check_room).
  checkingRoom,

  /// Обратный отсчёт перед стартом (бот-комнаты).
  countdown,

  /// Игра идёт — вопросы и ответы.
  playing,

  /// Все ответили, ожидание результатов.
  waitingResults,

  /// Игра завершена, показ результатов.
  finished,

  /// Ошибка / матч прерван.
  error,

  /// Переподключение.
  reconnecting,
}
